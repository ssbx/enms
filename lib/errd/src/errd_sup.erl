%%%-------------------------------------------------------------------
%%% @doc Supervises Erlang RRD processes.
%%% @end
%%% @author Geoff Cant <nem@lisp.geek.nz>
%%%-------------------------------------------------------------------
-module(errd_sup).

-behaviour(supervisor).
%%--------------------------------------------------------------------
%% Include files
%%--------------------------------------------------------------------

%%--------------------------------------------------------------------
%% External exports
%%--------------------------------------------------------------------
-export([start_link/0
        ]).

%%--------------------------------------------------------------------
%% Internal exports
%%--------------------------------------------------------------------
-export([init/1]).

%%--------------------------------------------------------------------
%% Macros
%%--------------------------------------------------------------------
-define(SERVER, ?MODULE).

%%--------------------------------------------------------------------
%% Records
%%--------------------------------------------------------------------

%%====================================================================
%% External functions
%%====================================================================
%%--------------------------------------------------------------------
%% @doc Starts the supervisor.
%% @spec start_link() -> {ok, pid()} | Error
%% @end
%%--------------------------------------------------------------------
start_link() ->
    supervisor:start_link({local, ?SERVER}, ?MODULE, []).

%%====================================================================
%% Server functions
%%====================================================================
%%--------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}   
%%--------------------------------------------------------------------
init([]) ->
    {ok,
        {
            {one_for_one, 1000, 3600},
	        [
	            {errd_server_sup,
	            {errd_server_sup, start_link, []},
	            permanent,
	            1000,
	            supervisor,
	            [errd_server]}
	        ]
        }
    }.
%%====================================================================
%% Internal functions
%%====================================================================

